// Bakes a verbatim copy of @keyma/runtime-js's pure type declarations into a
// string constant the JS backend inlines as a dependency-free `types.d.ts` in
// every generated bundle. Runs as `prebuild` so the copy never drifts; the
// committed output also lets the published package work without re-running it.
//
// `runtime-js/src/types.ts` is kept free of imports and runtime values (the
// brand*() helpers live in brand.ts) so it is valid `.d.ts` content as-is.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const here = path.dirname(fileURLToPath(import.meta.url));
const SOURCE = path.resolve(here, "../../runtime-js/src/types.ts");
const OUT = path.resolve(here, "../src/emitted-runtime-types.ts");

const header =
    "// Inlined from @keyma/runtime-js so generated bundles carry their own type\n" +
    "// surface and depend on no Keyma package at the type level.\n\n";

const body = readFileSync(SOURCE, "utf8");

const file =
    "// AUTO-GENERATED by scripts/gen-emitted-types.mjs from @keyma/runtime-js/src/types.ts.\n" +
    "// Do not edit by hand — run `npm run -w @keyma/compiler-backend-js build` to regenerate.\n" +
    `export const EMITTED_RUNTIME_TYPES_DTS = ${JSON.stringify(header + body)};\n`;

writeFileSync(OUT, file);
