// Bakes the COMPILER-OWNED runtime BEHAVIOR — the model codec + the @Service RPC stack authored
// in `@keyma/runtime/src` — into a string-keyed manifest the JS backend emits as bundle-local
// modules (siblings to the generated `types.js`/`services.js`). Generated bundles import the
// codec/host/client/transport from these baked modules and depend on NO `@keyma/runtime` package
// for behavior — the self-containment guarantee, extended from *types* to *types + behavior*.
//
// Each authored TS module is compiled (via the TypeScript API) to BOTH `.js` (the runtime, with
// types stripped) and `.d.ts` (so the generated `services.d.ts` and `index.d.ts` resolve the
// `ServiceClient`/`ServiceHost`/… surfaces they import). The behavior modules import their type
// surface from `./types.js` (the gen-emitted-types blob) and each other — a closed set with no
// external package import. `runtime/src` is the SOURCE and is never modified.
//
// Runs as `prebuild`/`pretest` so the copy never drifts; the committed output also lets the
// published package work without re-running it.

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";
import ts from "typescript";

const here = path.dirname(fileURLToPath(import.meta.url));
const RUNTIME_SRC = path.resolve(here, "../../../../runtime/src");
const OUT = path.resolve(here, "../emitted-runtime-modules.ts");

// The behavior modules baked into every bundle, in dependency order (low-level first). `types`
// is NOT here — its surface is baked separately by `gen-emitted-types.mjs` into `types.d.ts`.
const MODULES = [
    "fields",
    "varint",
    "base64",
    "serialize",
    "deserialize",
    "binary",
    "errors",
    "rpc",
    "service-host",
    "direct-transport",
    "client",
];

const rootFiles = MODULES.map((m) => path.join(RUNTIME_SRC, m + ".ts"));
// `types.ts` is a dependency (the modules import its types); include it so resolution succeeds,
// but its emitted output is discarded (the bundle's `types.d.ts` comes from gen-emitted-types).
const allFiles = [...rootFiles, path.join(RUNTIME_SRC, "types.ts")];

const options = {
    target: ts.ScriptTarget.ES2022,
    module: ts.ModuleKind.NodeNext,
    moduleResolution: ts.ModuleResolutionKind.NodeNext,
    declaration: true,
    removeComments: false,
    noEmitOnError: false,
    skipLibCheck: true,
    // Mirror the runtime's strictness so discriminated-union narrowing works and the emitted
    // declarations match what the runtime build produces.
    strict: true,
    exactOptionalPropertyTypes: true,
    noUncheckedIndexedAccess: true,
};

const outputs = new Map(); // absolute output path -> content
const host = ts.createCompilerHost(options);
host.writeFile = (fileName, content) => { outputs.set(path.resolve(fileName), content); };

const program = ts.createProgram(allFiles, options, host);
const result = program.emit();

const errors = ts.getPreEmitDiagnostics(program).concat(result.diagnostics)
    .filter((d) => d.category === ts.DiagnosticCategory.Error);
if (errors.length > 0) {
    const text = ts.formatDiagnosticsWithColorAndContext(errors, host);
    process.stderr.write(text + "\n");
    process.exit(1);
}

const manifest = {};
for (const m of MODULES) {
    const js = outputs.get(path.join(RUNTIME_SRC, m + ".js"));
    const dts = outputs.get(path.join(RUNTIME_SRC, m + ".d.ts"));
    if (js === undefined || dts === undefined) {
        process.stderr.write(`gen-emitted-runtime: missing emitted output for module "${m}"\n`);
        process.exit(1);
    }
    manifest[m] = { js, dts };
}

const file =
    "// AUTO-GENERATED by scripts/gen-emitted-runtime.mjs from runtime/src/*.ts.\n" +
    "// Do not edit by hand — run `npm run -w @keyma/compiler build` to regenerate.\n" +
    "// The model codec + @Service RPC stack, baked as bundle-local modules so generated code\n" +
    "// imports no @keyma/runtime package for behavior.\n\n" +
    "export type EmittedModule = { js: string; dts: string };\n\n" +
    "/** Bundle-relative module name → its baked `.js` + `.d.ts`, in dependency order. */\n" +
    `export const EMITTED_RUNTIME_MODULES: Record<string, EmittedModule> = ${JSON.stringify(manifest, null, 2)};\n\n` +
    `export const EMITTED_RUNTIME_MODULE_ORDER: readonly string[] = ${JSON.stringify(MODULES)};\n`;

writeFileSync(OUT, file);
