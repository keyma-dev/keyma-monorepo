// Bakes a verbatim copy of @keyma/runtime-cpp's runtime header into a string constant
// the C++ backend can emit inline for the `vendorRuntime` opt-in. Runs as `prebuild`
// (and `pretest`) so the copy never drifts; the committed output also lets the published
// package work without re-running it. Mirrors the JS backend's gen-emitted-types.mjs.
//
// It reads the COMMITTED source header (not a dist/ artifact), so it is independent of
// npm-workspace build order: runtime-cpp is header-only and need not be "built" first.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const here = path.dirname(fileURLToPath(import.meta.url));
const SOURCE = path.resolve(here, "../../../../runtime-cpp/include/keyma/runtime.hpp");
const OUT = path.resolve(here, "../runtime-header.generated.ts");

const body = readFileSync(SOURCE, "utf8");

const file =
    "// AUTO-GENERATED by scripts/gen-runtime-header.mjs from @keyma/runtime-cpp/include/keyma/runtime.hpp.\n" +
    "// Do not edit by hand — run `npm run -w @keyma/compiler-backend-cpp build` to regenerate.\n" +
    `export const RUNTIME_HPP = ${JSON.stringify(body)};\n`;

writeFileSync(OUT, file);
