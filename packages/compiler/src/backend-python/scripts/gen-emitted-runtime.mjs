// Bakes the COMPILER-OWNED Python runtime (codec + RPC stack + intrinsics) from
// `packages/runtime-python/src/keyma/runtime/` into a single self-contained module string the
// Python backend emits into every bundle as `_keyma_rpc.py`. Generated code imports the codec /
// RPC helpers / intrinsics from there and depends on NO `keyma-runtime` package — the runtime
// package stays the authored source-of-truth + isolated test bed, never imported by generated code.
//
// The schema-domain Python slice (validators/formatters/defaults — emitted inline by the data
// model domain) is NOT baked here; this generator carries only the codec + RPC + intrinsic surface.
//
// Runs as `prebuild`/`pretest` so the committed copy never drifts. The runtime source is the SOURCE
// and is never modified.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const here = path.dirname(fileURLToPath(import.meta.url));
const SRC_DIR = path.resolve(here, "../../../../runtime-python/src/keyma/runtime");
const OUT = path.resolve(here, "../emitted-runtime.ts");

// The bundle-local module name generated code imports the codec / RPC stack / intrinsics from.
const MODULE_NAME = "_keyma_rpc";

// Source modules in dependency order (a base name precedes anything that references it at import
// time). Concatenated into one module; intra-package `from .x import` lines are stripped because
// every name is already defined earlier in the single combined module.
const MODULES = [
    "errors",
    "types",
    "_shared",
    "_iso",
    "fields",
    "intrinsics",
    "serialize",
    "deserialize",
    "binary",
    "marshal",
    "transport",
    "service_host",
    "client",
    "direct_transport",
];

/** Classify and collect each source file: drop `__future__` + intra-package (relative) imports,
 *  hoist stdlib/external imports (deduped, first-seen order), keep the rest as the body. */
const stdlibImports = [];
const seenImports = new Set();
const bodies = [];

for (const name of MODULES) {
    const text = readFileSync(path.join(SRC_DIR, `${name}.py`), "utf8");
    const bodyLines = [];
    for (const line of text.split("\n")) {
        if (/^from __future__ import/.test(line)) continue; // re-added once at the very top
        if (/^from \./.test(line)) continue; // intra-package import — name is defined elsewhere here
        if (/^(import |from )/.test(line)) {
            if (!seenImports.has(line)) {
                seenImports.add(line);
                stdlibImports.push(line);
            }
            continue;
        }
        bodyLines.push(line);
    }
    // Trim leading/trailing blank lines per module, then separate modules with a banner.
    while (bodyLines.length > 0 && bodyLines[0].trim() === "") bodyLines.shift();
    while (bodyLines.length > 0 && bodyLines[bodyLines.length - 1].trim() === "") bodyLines.pop();
    bodies.push(`# ── ${name} ${"─".repeat(Math.max(0, 70 - name.length))}\n${bodyLines.join("\n")}`);
}

const header =
    "# AUTO-GENERATED by @keyma/compiler/backend-python/scripts/gen-emitted-runtime.mjs.\n" +
    "# Baked from packages/runtime-python/src/keyma/runtime/ — the codec + RPC stack + intrinsics,\n" +
    "# inlined so generated Python bundles are self-contained (import no keyma-runtime package).\n" +
    "# Do not edit by hand — run `npm run -w @keyma/compiler build` to regenerate.\n";

const moduleSource =
    header + "\nfrom __future__ import annotations\n\n" + stdlibImports.join("\n") + "\n\n\n" + bodies.join("\n\n\n") + "\n";

const file =
    "// AUTO-GENERATED by scripts/gen-emitted-runtime.mjs from packages/runtime-python/src/keyma/runtime/.\n" +
    "// Do not edit by hand — run `npm run -w @keyma/compiler build` to regenerate.\n\n" +
    `// The bundle-local module name generated code imports the codec / RPC stack / intrinsics from.\n` +
    `export const EMITTED_PY_RUNTIME_MODULE = ${JSON.stringify(MODULE_NAME)};\n\n` +
    `// The self-contained Python runtime module source, emitted into each bundle as \`${MODULE_NAME}.py\`.\n` +
    `export const EMITTED_PY_RUNTIME = ${JSON.stringify(moduleSource)};\n`;

writeFileSync(OUT, file);
