// Bakes the SCHEMA-DOMAIN Python validation drivers — `validate` / `format` / `apply_defaults`
// (+ their shared `schema_fields` walker + the `_context` invoker) from
// `packages/runtime-python/src/keyma/runtime/` into a single self-contained module string the
// schema Python emitter pack emits into every bundle as `_keyma_schema.py`. A generated app imports
// `validate` / `format` / `apply_defaults` from there and calls them off a class's `.metadata`
// dict — depending on NO `keyma-runtime` package (the opt-in validation API).
//
// The codec + RPC stack is baked separately by the COMPILER (`_keyma_rpc.py`); these drivers are a
// SCHEMA-domain concern, so they are baked here. They are fully standalone — the only cross-module
// type they reference (`Metadata`) is annotation-only under `from __future__ import annotations`, so
// it is never resolved at runtime and the stripped intra-package import is harmless.
//
// Runs as `prebuild`/`pretest` so the committed copy never drifts. The runtime source is the SOURCE
// and is never modified. Mirrors the compiler's `backend-python/scripts/gen-emitted-runtime.mjs`.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const here = path.dirname(fileURLToPath(import.meta.url));
const SRC_DIR = path.resolve(here, "../../../../runtime-python/src/keyma/runtime");
const OUT = path.resolve(here, "../emitted-schema-runtime.ts");

// The bundle-local module name generated apps import the validation drivers from.
const MODULE_NAME = "_keyma_schema";

// Source modules in dependency order (a base name precedes anything that references it at import
// time). Concatenated into one module; intra-package `from .x import` lines are stripped because
// every name is already defined earlier in the single combined module (or annotation-only).
const MODULES = [
    "_context",
    "schema_fields",
    "validate",
    "format",
    "defaults",
];

/** Drop `__future__` + intra-package (relative) imports, hoist stdlib/external imports (deduped,
 *  first-seen order), keep the rest as the body. */
const stdlibImports = [];
const seenImports = new Set();
const bodies = [];

for (const name of MODULES) {
    const text = readFileSync(path.join(SRC_DIR, `${name}.py`), "utf8");
    const bodyLines = [];
    for (const line of text.split("\n")) {
        if (/^from __future__ import/.test(line)) continue; // re-added once at the very top
        if (/^from \./.test(line)) continue; // intra-package import — name is defined elsewhere here
        if (/^(import |from )/.test(line)) {
            if (!seenImports.has(line)) {
                seenImports.add(line);
                stdlibImports.push(line);
            }
            continue;
        }
        bodyLines.push(line);
    }
    while (bodyLines.length > 0 && bodyLines[0].trim() === "") bodyLines.shift();
    while (bodyLines.length > 0 && bodyLines[bodyLines.length - 1].trim() === "") bodyLines.pop();
    bodies.push(`# ── ${name} ${"─".repeat(Math.max(0, 70 - name.length))}\n${bodyLines.join("\n")}`);
}

const header =
    "# AUTO-GENERATED by @keyma/schema/backend-python/scripts/gen-schema-runtime.mjs.\n" +
    "# Baked from packages/runtime-python/src/keyma/runtime/{_context,schema_fields,validate,format,\n" +
    "# defaults}.py — the validate/format/apply_defaults drivers, inlined so generated Python bundles\n" +
    "# call them with no keyma-runtime dependency (the opt-in validation API).\n" +
    "# Do not edit by hand — run `npm run -w @keyma/schema build` to regenerate.\n";

const moduleSource =
    header + "\nfrom __future__ import annotations\n\n" + stdlibImports.join("\n") + "\n\n\n" + bodies.join("\n\n\n") + "\n";

const file =
    "// AUTO-GENERATED by scripts/gen-schema-runtime.mjs from packages/runtime-python/src/keyma/runtime/.\n" +
    "// Do not edit by hand — run `npm run -w @keyma/schema build` to regenerate.\n\n" +
    "// The bundle-local module name generated apps import the validation drivers from.\n" +
    `export const EMITTED_PY_SCHEMA_RUNTIME_MODULE = ${JSON.stringify(MODULE_NAME)};\n\n` +
    `// The self-contained validation driver module source, emitted into each bundle as \`${MODULE_NAME}.py\`.\n` +
    `export const EMITTED_PY_SCHEMA_RUNTIME = ${JSON.stringify(moduleSource)};\n`;

writeFileSync(OUT, file);
