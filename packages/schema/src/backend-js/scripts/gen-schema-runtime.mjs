// Bakes the SCHEMA-DOMAIN runtime BEHAVIOR — the validate / format / applyDefaults drivers
// authored in `@keyma/runtime/src` — into a string-keyed manifest the schema JS emitter pack
// emits as bundle-local modules (siblings to the generated `types.js`/`services.js`). A generated
// app imports `validate`/`format`/`applyDefaults` from these baked modules and depends on NO
// `@keyma/runtime` package for behavior — the self-containment guarantee, mirroring the compiler's
// own `gen-emitted-runtime.mjs` for the codec/RPC stack.
//
// The codec/RPC modules are baked by the COMPILER (`compiler/src/backend-js/.../gen-emitted-runtime
// .mjs`); these schema-domain validation drivers belong to the SCHEMA domain (they consume the
// `SchemaMetadata`/`ValidatorFn` surface the schema pack slices into every bundle's `types.d.ts`),
// so they are baked here, next to the schema metadata builder, and contributed via the schema
// pack's `emitBundleFiles` hook.
//
// Each authored TS module is compiled (via the TypeScript API) to BOTH `.js` (types stripped) and
// `.d.ts`. The drivers import their type surface from `./types.js` (the schema pack's
// `gen-schema-types` blob, already in every bundle) and each other (`./schema-fields.js`) — a
// closed set with no external package import. `runtime/src` is the SOURCE and is never modified.
//
// Runs as `prebuild`/`pretest` so the copy never drifts; the committed output also lets the
// published package work without re-running it.

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";
import ts from "typescript";

const here = path.dirname(fileURLToPath(import.meta.url));
const RUNTIME_SRC = path.resolve(here, "../../../../runtime/src");
const OUT = path.resolve(here, "../emitted-schema-runtime.ts");

// The validation drivers baked into every bundle, in dependency order (low-level first).
// `schema-fields` is the shared field walker the three drivers import. `types` is NOT here — its
// surface is baked separately by `gen-schema-types.mjs` into each bundle's `types.d.ts`.
const MODULES = [
    "schema-fields",
    "validate",
    "format",
    "defaults",
];

const rootFiles = MODULES.map((m) => path.join(RUNTIME_SRC, m + ".ts"));
// `types.ts` is a dependency (the drivers import its types); include it so resolution succeeds,
// but its emitted output is discarded (the bundle's `types.d.ts` comes from gen-schema-types).
const allFiles = [...rootFiles, path.join(RUNTIME_SRC, "types.ts")];

const options = {
    target: ts.ScriptTarget.ES2022,
    module: ts.ModuleKind.NodeNext,
    moduleResolution: ts.ModuleResolutionKind.NodeNext,
    declaration: true,
    removeComments: false,
    noEmitOnError: false,
    skipLibCheck: true,
    strict: true,
    exactOptionalPropertyTypes: true,
    noUncheckedIndexedAccess: true,
};

const outputs = new Map(); // absolute output path -> content
const host = ts.createCompilerHost(options);
host.writeFile = (fileName, content) => { outputs.set(path.resolve(fileName), content); };

const program = ts.createProgram(allFiles, options, host);
const result = program.emit();

const errors = ts.getPreEmitDiagnostics(program).concat(result.diagnostics)
    .filter((d) => d.category === ts.DiagnosticCategory.Error);
if (errors.length > 0) {
    const text = ts.formatDiagnosticsWithColorAndContext(errors, host);
    process.stderr.write(text + "\n");
    process.exit(1);
}

const manifest = {};
for (const m of MODULES) {
    const js = outputs.get(path.join(RUNTIME_SRC, m + ".js"));
    const dts = outputs.get(path.join(RUNTIME_SRC, m + ".d.ts"));
    if (js === undefined || dts === undefined) {
        process.stderr.write(`gen-schema-runtime: missing emitted output for module "${m}"\n`);
        process.exit(1);
    }
    manifest[m] = { js, dts };
}

const file =
    "// AUTO-GENERATED by scripts/gen-schema-runtime.mjs from runtime/src/{schema-fields,validate,\n" +
    "// format,defaults}.ts. Do not edit by hand — run `npm run -w @keyma/schema build` to regenerate.\n" +
    "// The validate/format/applyDefaults drivers, baked as bundle-local modules so generated apps\n" +
    "// import no @keyma/runtime package for behavior (the opt-in validation API).\n\n" +
    "export type EmittedModule = { js: string; dts: string };\n\n" +
    "/** Bundle-relative module name → its baked `.js` + `.d.ts`, in dependency order. */\n" +
    `export const EMITTED_SCHEMA_RUNTIME_MODULES: Record<string, EmittedModule> = ${JSON.stringify(manifest, null, 2)};\n\n` +
    `export const EMITTED_SCHEMA_RUNTIME_MODULE_ORDER: readonly string[] = ${JSON.stringify(MODULES)};\n`;

writeFileSync(OUT, file);
